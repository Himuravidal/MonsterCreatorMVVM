package com.crisspian.monstercreator_mvvm.model;

public class MonsterAttributes {

    private int intelligence = 0;
    private int ugliness = 0;
    private int evilness = 0;

    public int getIntelligence() {
        return intelligence;
    }

    public void setIntelligence(int intelligence) {
        this.intelligence = intelligence;
    }

    public int getUgliness() {
        return ugliness;
    }

    public void setUgliness(int ugliness) {
        this.ugliness = ugliness;
    }

    public int getEvilness() {
        return evilness;
    }

    public void setEvilness(int evilness) {
        this.evilness = evilness;
    }
}
